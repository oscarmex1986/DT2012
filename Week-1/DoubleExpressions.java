
public class DoubleExpressions {
    public static void main(String[] args) {
        double x = 5.5, y = 2.2;
        System.out.println("Addition: " + (x + y));
        System.out.println("Multiplication: " + (x * y));
        System.out.println("Random number (0 to 1): " + Math.random());
    }
}
